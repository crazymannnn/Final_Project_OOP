    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        int selected = coursetable.getSelectedRow();
        if (selected != -1) {
            String courseID = (String) coursetable.getValueAt(selected, 0);
            for (int i = 0; i < mCourse.getListCourse().size(); i++) {
                if (mCourse.getListCourse().get(i).getId().equals(courseID)) {
                    var currentCourse = mCourse.getListCourse().get(i);
                    courseInfo.fillInTable(currentCourse);
                    courseInfo.setVisible(true);
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnDetailActionPerformed

