    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        try {
            var name = namet.getText();
            var id = idt.getText();
            int fee = Integer.parseInt(feet.getText());
            //var day = dayt.getText();
            var dayy = daytt.getSelectedItem().toString();
            //var time = timet.getText();
            var timet = timett.getSelectedItem().toString();
            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fill in ID");
                idt.requestFocus();
                return;
            } else if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fill in Name");
                namet.requestFocus();
                return;
            }
            Course course = new Course(id, name, fee, dayy, timet);
            if (mCourse.addCourse(course)) {
                fillInCourseTable();
                JOptionPane.showMessageDialog(this, "Add successfully");
                clearInfo();
            } else {
                JOptionPane.showMessageDialog(this, "ID already exist");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Fee must be number");
            feet.requestFocus();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selected = coursetable.getSelectedRow();
        if (selected != -1) {
            int answer = JOptionPane.showConfirmDialog(null, "Do you want to delete this row?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (answer == JOptionPane.YES_OPTION) {
                mCourse.deleteCourse((String) coursetable.getValueAt(selected, 0));
                fillInCourseTable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please choose row you want to delete");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selected = coursetable.getSelectedRow();
        if (selected != -1) {
            int answer = JOptionPane.showConfirmDialog(null, "Do you want to update this row?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (answer == JOptionPane.YES_OPTION) {
                var name = namet.getText();
                var id = idt.getText();
                int fee = Integer.parseInt(feet.getText());
                //var day = dayt.getText();
                var dayy = daytt.getSelectedItem().toString();
                //var time = timet.getText();
                var timet = timett.getSelectedItem().toString();
                Course course = new Course(id, name, fee, dayy, timet);
                mCourse.deleteCourse((String) coursetable.getValueAt(selected, 0));
                mCourse.addCourse(course);
                fillInCourseTable();
                JOptionPane.showMessageDialog(this, "Edit successfully");
                clearInfo();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please choose row you want to delete");
        }
    }//GEN-LAST:event_btnEditActionPerformed

